import { User } from "../../components/leaderboard/list";

export function useLeaderboardData(): User[] {
  const addresses = [
    "0x1234...5678",
    "0x2345...6789",
    "0x3456...7890",
    "0x4567...8901",
    "0x5678...9012",
    "0x6789...0123",
    "0x7890...1234",
    "0x8901...2345",
    "0x9012...3456",
    "0x0123...4567",
    "0xabcd...ef01",
    "0xbcde...f012",
    "0xcdef...0123",
    "0xdef0...1234",
    "0xef01...2345",
    "0xf012...3456",
    "0x0123...4567",
    "0x1234...5678",
    "0x2345...6789",
    "0x3456...7890",
    "0x4567...8901",
    "0x5678...9012",
    "0x6789...0123",
    "0x7890...1234",
    "0x8901...2345",
    "0x9012...3456",
    "0x0123...4567",
    "0x1234...5678",
    "0x2345...6789",
    "0x3456...7890",
    "0x4567...8901",
    "0x5678...9012",
    "0x6789...0123",
    "0x7890...1234",
    "0x8901...2345",
    "0x9012...3456",
    "0x0123...4567",
    "0x1234...5678",
    "0x2345...6789",
    "0x3456...7890",
    "0x4567...8901",
    "0x5678...9012",
    "0x6789...0123",
    "0x7890...1234",
    "0x8901...2345",
    "0x9012...3456",
    "0x0123...4567",
    "0x1234...5678",
    "0x2345...6789",
    "0x3456...7890",
  ];
  const data: User[] = [];

  const scoreVariations = [
    5, -3, 8, 2, -1, 12, 7, -5, 15, 3, -2, 9, 4, -7, 11, 6, -4, 13, 1, -6,
  ];

  for (let i = 1; i <= 50; i++) {
    const baseScore =
      3000 - (i - 1) * 45 + scoreVariations[i % scoreVariations.length];
    const level = Math.max(1, Math.floor(baseScore / 250));

    data.push({
      rank: i,
      address:
        addresses[i % addresses.length] ||
        `0x${i.toString(16).padStart(4, "0")}...${(i * 123)
          .toString(16)
          .slice(-4)}`,
      score: baseScore,
      level,
    });
  }

  return data;
}
